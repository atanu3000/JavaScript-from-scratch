// Boolean & comparison operator 
// boolean are either true or false

let num1 = 10;
let num2 = 15;

console.log(num1 > num2);
console.log(num1 < num2);
console.log(num1 == num2);
console.log(num1 != num2);

num1 = "15";

console.log(num1 == num2);
console.log(num1 === num2); // === checking value as well as type also

console.log(num1 != num2);
console.log(num1 !== num2);